void solve(){
	ll c,p,n1,n2,t1,t2;
	cin >> c >> p >> n1 >> n2 >> t1 >> t2;
	ll ans = 0;
	ll l = 0, r = c;
	while(l <= r){
		ll m = l + (r - l + 1)/2;
		if(m*p + m/n1*t1 + m/n2*t2 <= c){
			ans = m;
			l = m + 1;
		}
		else r = m - 1;
	}
	cout << ans << nl;
}
